# Use a variable to represent a person's name, and then print that person's name in lowercase, uppercase, and title case.
# Title Case
first_name = "kellen"
last_name = "land"
message = f"{first_name.title()} {last_name.title()}"
print(message)
# Uppercase
message_uppercase = f"{first_name.upper()} {last_name.upper()}"
print(message_uppercase)
# Lowercase
first_name_upper = "KELLEN"
last_name_upper = "LAND"
message_lowercase = f"{first_name_upper.lower()} {last_name_upper.lower()}"
print(message_lowercase)